create or replace function public.is_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid() and role = 'admin'
  );
$$;

create or replace function public.is_manager()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid() and role = 'gestor'
  );
$$;

create or replace function public.managed_unit_id()
returns bigint
language sql
security definer
set search_path = public
as $$
  select unit_id
  from public.profiles
  where id = auth.uid()
  limit 1
$$;

create or replace function public.is_admin_or_manager_of(target_unit_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.is_admin()
    or (
      public.is_manager()
      and public.managed_unit_id() = target_unit_id
    )
$$;

create or replace function public.can_manage_schedule(target_schedule_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.is_admin()
    or exists (
      select 1
      from public.schedules s
      join public.profiles p on p.id = auth.uid()
      where s.id = target_schedule_id
        and p.role = 'gestor'
        and p.unit_id = s.unit_id
    )
$$;

