create table if not exists public.doctors (
  id bigint generated by default as identity primary key,
  unit_id bigint not null references public.units (id) on delete cascade,
  specialty_id bigint not null references public.specialties (id) on delete restrict,
  full_name text not null,
  crm text not null,
  notes text,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.profiles (
  id uuid primary key references auth.users (id) on delete cascade,
  email text unique,
  cpf text unique,
  phone text,
  full_name text,
  role text not null default 'paciente' check (role in ('admin', 'gestor', 'paciente')),
  unit_id bigint references public.units (id) on delete set null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.schedules (
  id bigint generated by default as identity primary key,
  unit_id bigint not null references public.units (id) on delete cascade,
  specialty_id bigint not null references public.specialties (id) on delete cascade,
  doctor_id bigint references public.doctors (id) on delete set null,
  starts_at timestamptz not null,
  is_available boolean not null default true,
  status text not null default 'disponivel' check (status in ('disponivel', 'reservado', 'indisponivel')),
  created_at timestamptz not null default timezone('utc', now()),
  unique (unit_id, specialty_id, starts_at)
);

create table if not exists public.appointments (
  id bigint generated by default as identity primary key,
  patient_id uuid not null references public.profiles (id) on delete cascade,
  schedule_id bigint not null references public.schedules (id) on delete cascade,
  status text not null default 'pendente' check (status in ('pendente', 'confirmado', 'cancelado')),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create unique index if not exists appointments_active_schedule_unique
on public.appointments (schedule_id)
where status in ('pendente', 'confirmado');

