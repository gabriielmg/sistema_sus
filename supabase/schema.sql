create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, full_name, role, cpf, phone, unit_id)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', ''),
    coalesce(new.raw_user_meta_data ->> 'role', 'paciente'),
    coalesce(new.raw_user_meta_data ->> 'cpf', ''),
    coalesce(new.raw_user_meta_data ->> 'phone', ''),
    nullif(new.raw_user_meta_data ->> 'unit_id', '')::bigint
  )
  on conflict (id) do update
  set
    email = excluded.email,
    full_name = excluded.full_name,
    role = excluded.role,
    cpf = coalesce(nullif(excluded.cpf, ''), public.profiles.cpf),
    phone = coalesce(nullif(excluded.phone, ''), public.profiles.phone),
    unit_id = coalesce(excluded.unit_id, public.profiles.unit_id);

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_user();

create or replace function public.sync_schedule_status()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  active_exists boolean;
  target_schedule_id bigint;
begin
  target_schedule_id := coalesce(new.schedule_id, old.schedule_id);

  select exists (
    select 1
    from public.appointments
    where schedule_id = target_schedule_id
      and status in ('pendente', 'confirmado')
  )
  into active_exists;

  update public.schedules
  set
    is_available = not active_exists,
    status = case when active_exists then 'reservado' else 'disponivel' end
  where id = target_schedule_id;

  return coalesce(new, old);
end;
$$;

